def morse_to_text(morse):
    
    text = ''
    words = morse.split(WORDS_SPACE)
    for word in words:
        letters = word.split(LETTERS_SPACE)
        for letter in letters:
            for c,m in TEXT_TO_MORSE.items():
                if letter == m:
                    text += c
            text += ''
        text += ' '
    return text
    